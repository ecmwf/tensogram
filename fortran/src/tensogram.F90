   public :: TGM_VALUE_TYPE_NULL, TGM_VALUE_TYPE_BOOL, TGM_VALUE_TYPE_INT,   &
             TGM_VALUE_TYPE_FLOAT, TGM_VALUE_TYPE_STRING, TGM_VALUE_TYPE_BYTES, &
             TGM_VALUE_TYPE_ARRAY, TGM_VALUE_TYPE_MAP
