      integer(c_int) :: k
      k = c_tgm_value_get_type(self%ptr)
      if (k == TGM_VALUE_TYPE_ARRAY) then
         n = int(c_tgm_value_array_len(self%ptr))
      else if (k == TGM_VALUE_TYPE_MAP) then
         n = int(c_tgm_value_map_len(self%ptr))
      else
         n = 0
      end if
   end function value_len

