!> tensogram — Fortran 2008 interface to the Tensogram C ABI (libtensogram).
!> The rank-generic surface is built from explicit per-rank specifics, expanded
!> by the C preprocessor from src/tgm_*.inc (a `.F90` source), so the LIBRARY
!> compiles clean under `-std=f2008 -Wall -Wextra -Werror` — no assumed-rank or
!> SELECT RANK (both F2018). The `contiguous` attribute it leans on is F2008.
!>
!> gfortran note: the handle types pair a `final` procedure with default
!> initialization (the RAII + null-safety design, §5.4). That is valid F2008,
!> but gfortran's `-std=f2008` emits an f08/0011 advisory wherever such a type
!> appears as a LOCAL variable (application/test code, not this library). The
!> library itself has no such locals and stays warning-clean; consumers who
!> compile their own code with `-std=f2008 -Werror` should expect that advisory
!> (or use `-std=f2018`/`-std=gnu`).
!>
!> Shallow binding: a private `bind(C)` interface block mirrors the synchronous
!> subset of `tensogram.h`; the public `tensogram_*` procedures add idiomatic
!> Fortran strings, ownership (finalizers), the memory-order contract, and a
!> non-copyable handle guard.
!>
!> GENERIC ENCODE / DECODE
!>   `tensogram_encode` and `tensogram_to_array` are generic over dtype
!>   (real32, real64, int32, int64) and rank (0..7), via explicit per-rank
!>   specifics generated by the C preprocessor (src/tgm_ranks.inc drives the
!>   per-family templates). Ranks above 7 are a compile-time "no specific"
!>   error (vs. a runtime TGM_ERROR_OBJECT). (int8/int16/complex/float16 are
!>   documented follow-ups; Fortran has no native unsigned or half/complex-as-
!>   pairs scalar mapping.)
!>
!> MEMORY-ORDER CONTRACT
!>   A Fortran array `a(ni, nj, ...)` is written with the on-wire descriptor
!>   shape and strides REVERSED to C/row-major order. Consequences:
!>     * Round-trips Fortran <-> Fortran are bit-identical.
!>     * A NumPy / C reader sees the reversed (transposed) shape.
!>
!> OWNERSHIP / LIFETIMES (non-copyable owning-handle approach)
!>   `tensogram_buffer`  owns Rust-allocated encoded bytes (tgm_bytes_free).
!>   `tensogram_message` owns a decoded message handle  (tgm_message_free).
!>   Both free in a `final` procedure and via an idempotent `free` binding.
!>   The types are NON-COPYABLE: a defined `assignment(=)` calls `error stop`,
!>   so an accidental `b = a` aborts loudly at the copy site instead of
!>   aliasing the handle and double-freeing later. Pass handles by reference.
module tensogram
