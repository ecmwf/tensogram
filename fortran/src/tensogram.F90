#define FAM encode_
#define TGM_BODY "tgm_iface.inc"
#define KS f32
#include "tgm_ranks.inc"
#undef KS
#define KS f64
#include "tgm_ranks.inc"
#undef KS
#define KS i32
#include "tgm_ranks.inc"
#undef KS
#define KS i64
#include "tgm_ranks.inc"
#undef KS
#undef TGM_BODY
#undef FAM
   end interface tensogram_encode

