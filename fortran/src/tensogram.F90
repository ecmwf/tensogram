         tensogram_metadata_has = &
            c_tgm_metadata_has_at(meta%ptr, int(obj_index - 1, c_size_t), c_loc(key_c))
      else
         tensogram_metadata_has = c_tgm_metadata_has(meta%ptr, c_loc(key_c))
      end if
