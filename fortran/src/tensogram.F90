   function tensogram_metadata_reserved(meta) result(v)
      type(tensogram_metadata), intent(in) :: meta
      type(tensogram_value) :: v
      v%ptr = c_tgm_metadata_reserved(meta%ptr)
   end function tensogram_metadata_reserved

